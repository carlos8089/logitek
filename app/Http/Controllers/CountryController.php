     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        $countries = Country::all();
        $usersCount = '';
        $usersByCountry = collect([]);

        foreach($countries as $country){
            $usersCount = User::where('country', $country->id)->count();
            $usersByCountry->push(['name'=>$country->name],['number'=>$usersCount]);
        }
        return view('admin.countries', compact('countries','usersByCountry'));
    }

